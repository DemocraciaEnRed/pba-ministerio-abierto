export type BuenosAiresMunicipality = typeof BUENOS_AIRES_MUNICIPALITIES[number]

/** Indica si `value` es una provincia argentina válida. */
export function isValidProvince(value: string): value is Province {
  return (PROVINCES as readonly string[]).includes(value)
}

/** Indica si `value` es un municipio (partido) de la provincia de Buenos Aires. */
export function isValidBuenosAiresMunicipality(value: string): value is BuenosAiresMunicipality {
  return (BUENOS_AIRES_MUNICIPALITIES as readonly string[]).includes(value)
}
